    //Altera o médico selecionado
    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        //Se não tiver linha selecionado o retorno = -1
        if(jTable1.getSelectedRow() >= 0) {
            try {
                int idMedicoSelecionado = (int) jTable1.getValueAt(jTable1.getSelectedRow(),0);
                JFrame alterar = new MedicosAlterar(idMedicoSelecionado);
                alterar.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(MedicosListar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione UM médico!", "Médico", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

