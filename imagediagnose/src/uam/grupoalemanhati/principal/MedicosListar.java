    public static void atualizaTabela() throws SQLException {
    // Atualiza a tabela de Médicos
        
        DefaultTableModel tabela;
        tabela = (DefaultTableModel) jTable1.getModel();
        tabela.setNumRows(0);

        List<Medico> medicos = new MedicoDao().lista();
        
        for (int linha = 0; linha < medicos.size(); linha++) {
            
            Medico medico = medicos.get(linha);
            
            tabela.addRow(new Object[] {1});
            tabela.setValueAt(medico.getId(), linha, 0);
            tabela.setValueAt(medico.getNome(), linha, 1);
           
        }
        
    }


