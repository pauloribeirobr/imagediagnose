    public static void atualizaTabela(Paciente paciente) throws SQLException, ParseException {
        // Atualiza a tabela de Médicos
        
        DefaultTableModel tabela;
        tabela = (DefaultTableModel) jTable1.getModel();
        tabela.setNumRows(0);

        List<Anamnese> anamneses = new AnamneseDao().listaPaciente(paciente);
        
        for (int linha = 0; linha < anamneses.size(); linha++) {
            
            Anamnese anamnese = anamneses.get(linha);
            
            tabela.addRow(new Object[] {1});
            tabela.setValueAt(anamnese.getId(), linha, 0);
            //Formata a Data
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            tabela.setValueAt(sdf.format(anamnese.getData()), linha, 1);
            //tabela.setValueAt(anamnese.getPaciente().getNome(), linha, 2);
            tabela.setValueAt(anamnese.getMedico().getNome(), linha, 2);
            tabela.setValueAt(anamnese.getQueixa(), linha, 3);
        }
        
    }

