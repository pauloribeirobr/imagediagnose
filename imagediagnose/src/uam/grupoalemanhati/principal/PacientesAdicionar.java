    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Adiciona um paciente
        String nome = jTextField1.getText();
        String nascimento = jFormattedTextField1.getText();
        int idade = Integer.parseInt(jTextField4.getText());
        int altura = Integer.parseInt(jTextField2.getText());
        int peso = Integer.parseInt(jTextField3.getText());
        String sexo = (String) jComboBox1.getSelectedItem();
        
        Paciente paciente = new Paciente();
        paciente.setNome(nome);
        paciente.setDataDeNascimento(nascimento);
        paciente.setIdade(idade);
        paciente.setAltura(altura);
        paciente.setPeso(peso);
        paciente.setSexo(sexo);

        try {
            
            new PacienteDao().inserir(paciente);
            //Atualiza a tabela
            PacientesListar.atualizaTabela();
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(MedicosAdicionar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

